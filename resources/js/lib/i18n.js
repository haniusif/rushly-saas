export function useLocale() {
    const { props } = usePage();
    return props?.app?.locale || 'en';
}

export function useT() {
    const locale = useLocale();
    const dict = DICTIONARY[locale] || DICTIONARY.en;
    return (key) => dict[key] ?? DICTIONARY.en[key] ?? key;
}
